import os
import time
import argparse
import itertools
from train import train

# ── Experiment configuration ──────────────────────────────────────────────────
ALGORITHMS   = ["PPO", "SAC", "TD3", "A2C"]
CONDITIONS   = ["no_curriculum", "manual", "adaptive"]
SEEDS        = [0, 1]
TOTAL_TIMESTEPS = 1_000_000

# Hardcore extension
HARDCORE_ALGORITHMS  = ["PPO", "SAC", "TD3", "A2C"]
HARDCORE_SEEDS       = [0, 1]


# ── Build run list ────────────────────────────────────────────────────────────
def get_primary_runs():
    """Returns all 24 primary runs as a list of (algo, condition, seed)."""
    return list(itertools.product(ALGORITHMS, CONDITIONS, SEEDS))


def get_hardcore_runs():
    """Returns all 8 Hardcore extension runs."""
    return list(itertools.product(
        HARDCORE_ALGORITHMS, ["hardcore"], HARDCORE_SEEDS
    ))


# ── Check which runs are already done ────────────────────────────────────────
def get_pending_runs(runs, model_base_dir="models"):
    """
    Filters out runs that already have a saved final model.
    Allows safe re-running after interruptions.
    """
    pending = []
    for algo, condition, seed in runs:
        run_name = f"{algo}_{condition}_seed{seed}"
        model_path = os.path.join(
            model_base_dir, run_name, "final_model.zip"
        )
        if os.path.exists(model_path):
            print(f"[SKIP] Already done: {run_name}")
        else:
            pending.append((algo, condition, seed))
    return pending


# ── Sequential runner ─────────────────────────────────────────────────────────
def run_sequential(runs, timesteps=TOTAL_TIMESTEPS):
    """
    Runs all experiments one after another.
    Use this on a single GPU.
    """
    total = len(runs)
    completed = 0
    failed = []

    print(f"\nStarting sequential run: {total} experiments")
    print(f"Estimated time: {total * 1.5:.0f} – {total * 2.5:.0f} hours\n")

    for i, (algo, condition, seed) in enumerate(runs):
        print(f"\n[{i+1}/{total}] {algo} | {condition} | seed {seed}")
        start = time.time()

        try:
            train(
                algo_name=algo,
                condition=condition,
                seed=seed,
                timesteps=timesteps,
            )
            elapsed = time.time() - start
            completed += 1
            print(f"  Completed in {elapsed/60:.1f} min")

        except Exception as e:
            print(f"  FAILED: {e}")
            failed.append((algo, condition, seed))

    print(f"\n{'='*50}")
    print(f"Completed: {completed}/{total}")
    if failed:
        print(f"Failed runs:")
        for run in failed:
            print(f"  {run[0]} | {run[1]} | seed {run[2]}")
    print(f"{'='*50}")


# ── Parallel runner ───────────────────────────────────────────────────────────
import multiprocessing as mp

def worker(args):
    algo, condition, seed, timesteps = args
    try:
        train(
            algo_name=algo,
            condition=condition,
            seed=seed,
            timesteps=timesteps,
        )
        return (algo, condition, seed, "success")
    except Exception as e:
        return (algo, condition, seed, f"failed: {e}")


def run_parallel(runs, n_jobs=4, timesteps=TOTAL_TIMESTEPS):
    """
    Runs experiments in parallel using multiprocessing.
    """

    print(f"\nStarting parallel run: {len(runs)} experiments, {n_jobs} workers")

    # attach timesteps to each job
    runs_with_steps = [
        (algo, condition, seed, timesteps)
        for (algo, condition, seed) in runs
    ]

    with mp.Pool(processes=n_jobs) as pool:
        results = pool.map(worker, runs_with_steps)

    for algo, condition, seed, status in results:
        print(f"  {algo} | {condition} | seed {seed}: {status}")



# ── Progress report ───────────────────────────────────────────────────────────
def print_progress(model_base_dir="models"):
    """
    Prints a summary of which runs are done and which are pending.
    Run this anytime to check progress.
    """
    all_runs = get_primary_runs() + get_hardcore_runs()
    total = len(all_runs)
    done = 0

    print(f"\n{'='*55}")
    print(f"  EXPERIMENT PROGRESS")
    print(f"{'='*55}")
    print(f"  {'Algorithm':<8} {'Condition':<18} {'Seed':<6} {'Status'}")
    print(f"  {'-'*50}")

    for algo, condition, seed in all_runs:
        run_name = f"{algo}_{condition}_seed{seed}"
        model_path = os.path.join(
            model_base_dir, run_name, "final_model.zip"
        )
        status = "✓ Done" if os.path.exists(model_path) else "○ Pending"
        if os.path.exists(model_path):
            done += 1
        print(f"  {algo:<8} {condition:<18} {seed:<6} {status}")

    print(f"\n  Progress: {done}/{total} runs completed")
    print(f"{'='*55}\n")


# ── Entry point ───────────────────────────────────────────────────────────────
if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Run BipedalWalker curriculum experiments"
    )
    parser.add_argument(
        "--mode",
        choices=["primary", "hardcore", "all", "progress"],
        default="primary",
        help="Which experiments to run"
    )
    parser.add_argument(
        "--parallel",
        action="store_true",
        help="Run experiments in parallel"
    )
    parser.add_argument(
        "--n_jobs",
        type=int,
        default=4,
        help="Number of parallel workers"
    )
    parser.add_argument(
        "--timesteps",
        type=int,
        default=TOTAL_TIMESTEPS,
        help="Timesteps per run"
    )
    parser.add_argument(
        "--skip_done",
        action="store_true",
        default=True,
        help="Skip already completed runs"
    )
    args = parser.parse_args()

    # Just check progress
    if args.mode == "progress":
        print_progress()
        exit()

    # Build run list
    if args.mode == "primary":
        runs = get_primary_runs()
    elif args.mode == "hardcore":
        runs = get_hardcore_runs()
    else:
        runs = get_primary_runs() + get_hardcore_runs()

    # Filter already completed runs
    if args.skip_done:
        runs = get_pending_runs(runs)

    if not runs:
        print("All runs already completed.")
        print_progress()
        exit()

    print(f"\nRuns to execute: {len(runs)}")

    # Execute
    if args.parallel:
        run_parallel(runs, n_jobs=args.n_jobs, timesteps=args.timesteps)
    else:
        run_sequential(runs, timesteps=args.timesteps)

    print_progress()